package tunnel

// L-NEW-1 FIX: native Go fuzz targets for tunnel-layer network-facing parsers.
// Run with: go test -fuzz=FuzzNewDeliveryInstructions ./lib/tunnel/
// Each target feeds arbitrary bytes into a parser and checks for panics and
// index-out-of-bounds errors. No crash == no finding.

import "testing"

// FuzzNewDeliveryInstructions exercises the DeliveryInstructions parser.
// Tunnel data messages from any participant contain delivery instructions
// at the front; a panic here crashes the tunnel participation loop.
func FuzzNewDeliveryInstructions(f *testing.F) {
	f.Add([]byte{})
	// LOCAL delivery (type 0): flag byte 0x00 + minimal fragment
	f.Add([]byte{0x00, 0x00, 0x00, 0x00})
	// TUNNEL delivery (type 3): flag 0x30 = 0b00110000
	f.Add([]byte{0x30, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00})
	// DESTINATION delivery (type 1): flag 0x10
	f.Add([]byte{0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00})
	// Follow-on fragment: flag 0x80 | (fragNum << 1) | lastBit
	f.Add([]byte{0x80, 0x00, 0x00, 0x00, 0x01, 0x00, 0x10})
	f.Add(make([]byte, 100))
	f.Fuzz(func(t *testing.T, data []byte) {
		_, _ = NewDeliveryInstructions(data)
	})
}
